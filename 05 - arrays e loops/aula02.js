
/* 
    Em JavaScript é possível ter vários tipos dentro de um array
*/

const alunos = ['João', 'Vitor', 'Marina'];

/* Adiciona por último */
alunos.push(10);

alunos[4] = 'Gustavo';

console.log(alunos);

/* Remove o último */
alunos.pop();

console.log(alunos);

/* Remove o primeiro */
alunos.shift();

console.log(alunos);


